package com.jsp.springbootfirst;

public class Person {
	private int id;
	 int age;
	String name;//getter and setters method are mandatory bcoz we get json object
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
